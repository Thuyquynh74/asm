<script>
export default {
    data() {
        return {
            newComment: '',
            comments: JSON.parse(localStorage.getItem('comments')) || []
        };
    },
    computed: {
        username() {
            return localStorage.getItem('username') || null;
        }
    },
    methods: {
        // Phương thức đăng xuất
        logout() {
            localStorage.removeItem('username'); // Xóa username khỏi localStorage
            this.$router.push('/'); // Chuyển hướng về trang login
        },

            postComment() {
                if (!this.username) {
                    alert('Please login first!');
                    this.$router.push('/');
                    return;
                }

                // Lấy thời gian hiện tại
                const timestamp = new Date().toLocaleString();

                // Tạo đối tượng comment mới
                const newCommentObj = {
                    username: this.username,
                    text: this.newComment,
                    timestamp: timestamp
                };

                // Thêm comment mới vào mảng
                this.comments.push(newCommentObj);

                // Lưu lại vào localStorage
                localStorage.setItem('comments', JSON.stringify(this.comments));

                // Xóa nội dung comment sau khi post
                this.newComment = '';
            }
    },
    mounted() {
        // Kiểm tra nếu không có username trong localStorage, điều hướng về trang login
        if (!this.username) {
            this.$router.push('/');
        }
    }
};
</script>

